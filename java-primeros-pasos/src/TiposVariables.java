
public class TiposVariables {

	public static void main(String[] args) {
		System.out.println("nohelia");
		//enteros
		

		int edad = 130;
		System.out.println(edad);

		edad = 50 + 50;
		System.out.println("un siglo es " + edad);

		edad = 50 - 25;
		System.out.println("la mitad de 50 es " + edad);
		// decimales
		
		double salario = 1250.56;
		System.out.println(salario);

		double salarioMitad = salario / 2;
		System.out.println(salarioMitad);
		System.out.println(edad / 1);

		//conversiones
		
		double variableUno = 230.89;
		int varibaleUnoEntero = (int) variableUno;
		System.out.println(varibaleUnoEntero);
	}
	
	
}
